package com.tistory.uhhyunjoo.week01;

public class CharEx {
    public static void main(String[] args) {
        char ch = 'A';

        System.out.println(ch); // A
        System.out.println((int)ch); // 65

        ch = 66;
        System.out.println(ch); // B

        int ch2 = 67;
        System.out.println(ch2); // 67
        System.out.println((char)ch2); // C
    }
}
